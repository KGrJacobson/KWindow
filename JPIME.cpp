std::vector<std::string> Japanese_IME::GetKanji(std::string kana, int whichlist)
{
	switch (whichlist)
	{
	case ONYOMI:
		return kanjionmap_[kana];
	case KUNYOMI:
		return kanjikunmap_[kana];
	case NAME:
		return kanjinamemap_[kana];
	}

	return kanjionmap_[kana];
}
